<!-- JavaScript para actualizar estado del negocio dinámicamente -->
<script>
function updateBusinessStatus() {
    // Obtener hora actual en República Dominicana (GMT-4)
    const now = new Date();
    const utc = now.getTime() + (now.getTimezoneOffset() * 60000);
    const dominicanTime = new Date(utc + (-4 * 3600000)); // GMT-4
    
    const currentDay = dominicanTime.getDay(); // 0 = Domingo, 1 = Lunes, ... 6 = Sábado
    const currentHour = dominicanTime.getHours();
    const currentMinute = dominicanTime.getMinutes();
    const currentTime = currentHour + (currentMinute / 60);
    
    // Horarios de trabajo
    const weekdayOpen = 9;    // 9:00 AM
    const weekdayClose = 17.5; // 5:30 PM
    const saturdayOpen = 9;    // 9:00 AM
    const saturdayClose = 13;  // 1:00 PM
    
    const statusBadge = document.getElementById('business-status');
    const statusIcon = document.getElementById('status-icon');
    const statusText = document.getElementById('status-text');
    
    if (!statusBadge || !statusIcon || !statusText) return;
    
    let isOpen = false;
    let status = 'closed';
    let message = 'Cerrado';
    let iconClass = 'bi-x-circle-fill';
    
    // Verificar si es día laborable (Lunes a Viernes)
    if (currentDay >= 1 && currentDay <= 5) {
        if (currentTime >= weekdayOpen && currentTime < weekdayClose) {
            isOpen = true;
            status = 'open';
            message = 'Abierto Ahora';
            iconClass = 'bi-check-circle-fill';
        } else if (currentTime >= (weekdayOpen - 1) && currentTime < weekdayOpen) {
            // Abre en 1 hora
            status = 'opening-soon';
            message = 'Abre en 1 hora';
            iconClass = 'bi-clock-fill';
        } else if (currentTime >= (weekdayClose - 0.5) && currentTime < weekdayClose) {
            // Cierra en 30 min
            isOpen = true;
            status = 'open';
            message = 'Cierra en 30 min';
            iconClass = 'bi-exclamation-circle-fill';
        }
    }
    // Verificar si es sábado
    else if (currentDay === 6) {
        if (currentTime >= saturdayOpen && currentTime < saturdayClose) {
            isOpen = true;
            status = 'open';
            message = 'Abierto Ahora';
            iconClass = 'bi-check-circle-fill';
        } else if (currentTime >= (saturdayOpen - 1) && currentTime < saturdayOpen) {
            // Abre en 1 hora
            status = 'opening-soon';
            message = 'Abre en 1 hora';
            iconClass = 'bi-clock-fill';
        } else if (currentTime >= (saturdayClose - 0.5) && currentTime < saturdayClose) {
            // Cierra en 30 min
            isOpen = true;
            status = 'open';
            message = 'Cierra en 30 min';
            iconClass = 'bi-exclamation-circle-fill';
        }
    }
    // Domingo - cerrado todo el día
    else {
        status = 'closed';
        message = 'Cerrado - Domingo';
        iconClass = 'bi-x-circle-fill';
    }
    
    // Actualizar clases CSS
    statusBadge.className = `status-badge ${status}`;
    
    // Actualizar icono
    statusIcon.className = `bi ${iconClass}`;
    
    // Actualizar texto
    statusText.textContent = message;
    
    // Console log para debug (opcional)
    console.log(`Business Status Updated: ${message} at ${dominicanTime.toLocaleTimeString()}`);
}

// Ejecutar al cargar la página
document.addEventListener('DOMContentLoaded', function() {
    updateBusinessStatus();
    
    // Actualizar cada minuto
    setInterval(updateBusinessStatus, 60000);
});

// También ejecutar inmediatamente si el DOM ya está cargado
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', updateBusinessStatus);
} else {
    updateBusinessStatus();
}
</script>